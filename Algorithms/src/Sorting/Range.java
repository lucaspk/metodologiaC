package Sorting;

public class Range {
    private final int leftIndex;
    private final int rightIndex;

    public Range(int leftIndex, int rightIndex) {
        this.leftIndex = leftIndex;
        this.rightIndex = rightIndex;
    }

    public int getLeftIndex() {
        return leftIndex;
    }

    public int getRightIndex() {
        return rightIndex;
    }
}
